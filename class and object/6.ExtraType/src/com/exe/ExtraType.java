package com.exe;

public class ExtraType 
{
	private String name;
	private String runs;
	public ExtraType()
	{
		
	}
	public ExtraType(String name, String runs) {
		super();
		this.name = name;
		this.runs = runs;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getRuns() {
		return runs;
	}
	public void setRuns(String runs) {
		this.runs = runs;
	}

}
