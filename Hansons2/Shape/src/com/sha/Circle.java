package com.sha;

public class Circle extends Shape
{
private float radius;
public Circle()
{
	}

public Circle(float radius) {
	super();
	this.radius = radius;
}

public float getRadius() {
	return radius;
}

public void setRadius(float radius) {
	this.radius = radius;
}
double f=3.14;
public Double calculatePerimeter()
{
	return f*h*radius;
	}
}
